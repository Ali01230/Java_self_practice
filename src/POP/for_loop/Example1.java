package POP.for_loop;

public class Example1 {
    public static void main(String[] args) {


        //outer loop
        for(int cameraMan = 1; cameraMan <= 6; cameraMan++){
            System.out.println("Outer loop: " + cameraMan);

            //inner loop
            for(int cameraMan2 = 1; cameraMan2 <= 3; cameraMan2++){
                System.out.println("Inner loop: " + cameraMan2);
            }

        }


    }
}
