package OOP.polymorphism;

public class Quizz {
    public static void main(String[] args) {

      int a = 5;
      a += 5;

      switch (a){
          case 5:
              System.out.println("5");break;
          case 10:
              System.out.println("10");
          default:
              System.out.println("0");
      }
    }
}
